const month  = 3;

switch (month) {
    case 1:
        console.log("Jan");        
        break;
    case 2:
        console.log("Feb");        
        break;
    case 3:
        console.log("March");        
        break;

    default:
        console.log("Default case is there");
        break;
}

const name = "Keshav";
